mood = input("What is your mood today? ")

if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("Smile please!")
elif mood == "excited":
    print("So that's fantastic.")
elif mood == "relaxed":
    print("Relax and take a deep breath.")
else:
    print("I don't recognize this mood.")
